#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_world_points_count() {
        let points = get_world_points();
        assert!(
            points.len() >= 1500 && points.len() <= 2500,
            "Expected 1500-2500 points, got {}",
            points.len()
        );
    }

    #[test]
    fn test_coordinates_in_range() {
        for (i, (lat, lon)) in get_world_points().iter().enumerate() {
            assert!(
                *lat >= -90.0 && *lat <= 90.0,
                "Point {} has invalid latitude: {}",
                i,
                lat
            );
            assert!(
                *lon >= -180.0 && *lon <= 180.0,
                "Point {} has invalid longitude: {}",
                i,
                lon
            );
        }
    }

    #[test]
    fn test_has_all_continents() {
        let points = get_world_points();

        // Check for points in each continent's rough bounding box
        let has_north_america = points.iter().any(|(lat, lon)| *lat > 25.0 && *lon < -60.0);
        let has_south_america = points.iter().any(|(lat, lon)| *lat < -10.0 && *lon < -35.0);
        let has_europe = points
            .iter()
            .any(|(lat, lon)| *lat > 40.0 && *lon > -10.0 && *lon < 30.0);
        let has_africa = points
            .iter()
            .any(|(lat, lon)| *lat < 0.0 && *lon > 10.0 && *lon < 50.0);
        let has_asia = points.iter().any(|(lat, lon)| *lat > 20.0 && *lon > 60.0);
        let has_australia = points.iter().any(|(lat, lon)| *lat < -20.0 && *lon > 110.0);

        assert!(has_north_america, "Missing North America");
        assert!(has_south_america, "Missing South America");
        assert!(has_europe, "Missing Europe");
        assert!(has_africa, "Missing Africa");
        assert!(has_asia, "Missing Asia");
        assert!(has_australia, "Missing Australia");
    }
}
